namespace BootBlazorUI
{
    /// <summary>
    /// 横向对齐方式。
    /// </summary>
    public enum HorizontalAlignment
    {
        /// <summary>
        /// 左对齐。
        /// </summary>
        Left,
        /// <summary>
        /// 居中对齐。
        /// </summary>
        Center,
        /// <summary>
        /// 右对齐。
        /// </summary>
        Right
    }

    /// <summary>
    /// 垂直对齐方式。
    /// </summary>
    public enum VerticalAlignment
    {
        /// <summary>
        /// 顶部对齐。
        /// </summary>
        Top,
        /// <summary>
        /// 居中对齐。
        /// </summary>
        Middle,
        /// <summary>
        /// 底部对齐。
        /// </summary>
        Bottom
    }

    /// <summary>
    /// flex 布局的对齐方式。
    /// </summary>
    public enum Flex
    {
        /// <summary>
        /// 从顶端开始。
        /// </summary>
        Start,
        /// <summary>
        /// 从末端开始。
        /// </summary>
        End,
        /// <summary>
        /// 居中显示。
        /// </summary>
        Center,
        /// <summary>
        /// 两端显示。
        /// </summary>
        Between,
        /// <summary>
        /// 环绕显示。
        /// </summary>
        Around,
    }
}
